// ১. সংখ্যা (Number)
let age = 25; // পূর্ণ সংখ্যা
let price = 19.99; // দশমিক সংখ্যা
console.log("Age:", age); // Age: 25
console.log("Price:", price); // Price: 19.99

// ২. স্ট্রিং (String)
let name = "Alice"; // ডাবল কোটেশন
let greeting = 'Hello, World!'; // একক কোটেশন
console.log("Name:", name); // Name: Alice
console.log("Greeting:", greeting); // Greeting: Hello, World!

// ৩. বুলিয়ান (Boolean)
let isActive = true; // সত্য
let isLoggedIn = false; // মিথ্যা
console.log("Is Active:", isActive); // Is Active: true
console.log("Is Logged In:", isLoggedIn); // Is Logged In: false

// ৪. অবজেক্ট (Object)
let person = {
    name: "John",
    age: 30,
    isStudent: false
};
console.log("Person Object:", person);

// ৫. অ্যারে (Array)
let colors = ["Red", "Green", "Blue"];
console.log("Colors Array:", colors); // Colors Array: [ 'Red', 'Green', 'Blue' ]

// ৬. নাল (Null)
let emptyValue = null;
console.log("Empty Value:", emptyValue); // Empty Value: null

// ৭. অপরিবর্তনীয় (Undefined)
let notAssigned;
console.log("Not Assigned:", notAssigned); // Not Assigned: undefined
